function _generateSeveritySection(result) {
  const counts = result.comparison.summary.severityCounts;
  const total = result.comparison.summary.totalDifferences;
  
  if (total === 0) {
    return `
      <section class="section">
        <h2 class="section-title">Severity Breakdown</h2>
        <div class="no-data">No differences detected</div>
      </section>
    `;
  }
  
  return `
    <section class="section">
      <h2 class="section-title">Severity Breakdown</h2>
      <div class="severity-bars">
        ${counts.critical > 0 ? `
          <div class="severity-bar">
            <span class="severity-label critical">Critical</span>
            <div class="severity-progress">
              <div class="severity-fill critical" style="width: ${(counts.critical / total * 100).toFixed(1)}%"></div>
            </div>
            <span class="severity-count">${counts.critical}</span>
          </div>
        ` : ''}
        ${counts.high > 0 ? `
          <div class="severity-bar">
            <span class="severity-label high">High</span>
            <div class="severity-progress">
              <div class="severity-fill high" style="width: ${(counts.high / total * 100).toFixed(1)}%"></div>
            </div>
            <span class="severity-count">${counts.high}</span>
          </div>
        ` : ''}
        ${counts.medium > 0 ? `
          <div class="severity-bar">
            <span class="severity-label medium">Medium</span>
            <div class="severity-progress">
              <div class="severity-fill medium" style="width: ${(counts.medium / total * 100).toFixed(1)}%"></div>
            </div>
            <span class="severity-count">${counts.medium}</span>
          </div>
        ` : ''}
        ${counts.low > 0 ? `
          <div class="severity-bar">
            <span class="severity-label low">Low</span>
            <div class="severity-progress">
              <div class="severity-fill low" style="width: ${(counts.low / total * 100).toFixed(1)}%"></div>
            </div>
            <span class="severity-count">${counts.low}</span>
          </div>
        ` : ''}
      </div>
    </section>
  `;
}

function _generateDifferencesSection(result) {
  const rows = [];
  
  for (const match of result.comparison.results) {
    if (match.differences && match.differences.length > 0) {
      for (const diff of match.differences) {
        rows.push(`
          <tr>
            <td>${match.elementId}</td>
            <td>${match.tagName}</td>
            <td>${diff.property}</td>
            <td>${_escapeHTML(diff.baseValue || '')}</td>
            <td>${_escapeHTML(diff.compareValue || '')}</td>
            <td>${diff.category}</td>
            <td><span class="badge ${diff.severity}">${diff.severity}</span></td>
          </tr>
        `);
      }
    }
  }
  
  if (rows.length === 0) {
    return `
      <section class="section">
        <h2 class="section-title">Differences</h2>
        <div class="no-data">No differences detected</div>
      </section>
    `;
  }
  
  return `
    <section class="section">
      <h2 class="section-title">Differences (${rows.length})</h2>
      <table>
        <thead>
          <tr>
            <th>Element</th>
            <th>Tag</th>
            <th>Property</th>
            <th>Baseline Value</th>
            <th>Compare Value</th>
            <th>Category</th>
            <th>Severity</th>
          </tr>
        </thead>
        <tbody>
          ${rows.join('')}
        </tbody>
      </table>
    </section>
  `;
}

function _generateUnmatchedSection(result) {
  const baselineRows = result.unmatchedElements.baseline.map(el => `
    <tr>
      <td><span class="badge removed">Removed</span></td>
      <td>${el.id}</td>
      <td>${el.tagName}</td>
      <td>${el.elementId || '-'}</td>
      <td>${_escapeHTML(el.className || '-')}</td>
    </tr>
  `);
  
  const compareRows = result.unmatchedElements.compare.map(el => `
    <tr>
      <td><span class="badge added">Added</span></td>
      <td>${el.id}</td>
      <td>${el.tagName}</td>
      <td>${el.elementId || '-'}</td>
      <td>${_escapeHTML(el.className || '-')}</td>
    </tr>
  `);
  
  const allRows = [...baselineRows, ...compareRows];
  
  if (allRows.length === 0) {
    return `
      <section class="section">
        <h2 class="section-title">Unmatched Elements</h2>
        <div class="no-data">All elements matched successfully</div>
      </section>
    `;
  }
  
  return `
    <section class="section">
      <h2 class="section-title">Unmatched Elements (${allRows.length})</h2>
      <table>
        <thead>
          <tr>
            <th>Status</th>
            <th>Element ID</th>
            <th>Tag Name</th>
            <th>Element ID Attr</th>
            <th>Class Name</th>
          </tr>
        </thead>
        <tbody>
          ${allRows.join('')}
        </tbody>
      </table>
    </section>
  `;
}

function _escapeHTML(str) {
  if (typeof str !== 'string') return str;
  
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

