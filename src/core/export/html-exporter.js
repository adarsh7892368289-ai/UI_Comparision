import logger from '../../infrastructure/logger.js';

function exportToHTML(comparisonResult) {
  try {
    const htmlContent = _generateHTML(comparisonResult);
    
    const blob = new Blob([htmlContent], { type: 'text/html;charset=utf-8;' });
    const url = URL.createObjectURL(blob);
    
    const a = document.createElement('a');
    a.href = url;
    a.download = `comparison-report-${comparisonResult.baseline.id}-vs-${comparisonResult.compare.id}.html`;
    a.click();
    
    URL.revokeObjectURL(url);
    
    logger.info('HTML export complete');
    return { success: true };
  } catch (error) {
    logger.error('HTML export failed', { error: error.message });
    return { success: false, error: error.message };
  }
}

